package javaFromKotlin.reservedWordsInKotlin;

public class KeywordExample {
    // Method with 'in' keyword as identifier
    public void in() {
        System.out.println("Called in()");
    }

    // Method with 'object' keyword as identifier
    public void object() {
        System.out.println("Called object()");
    }

    // Method with 'is' keyword as identifier
    public void is() {
        System.out.println("Called is()");
    }
}